export type MantisTheme = typeof mantisTheme;

// Theme utility functions
export const getColor = (colorPath: string): string => {
  const keys = colorPath.split('.');
  let value: any = mantisTheme.colors;
  
  for (const key of keys) {
    value = value?.[key];
  }
  
  return value || colorPath;
};

export const getSpacing = (size: keyof typeof mantisTheme.spacing): string => {
  return mantisTheme.spacing[size];
};

export const getBorderRadius = (size: keyof typeof mantisTheme.borderRadius): string => {
  return mantisTheme.borderRadius[size];
};

export const getShadow = (size: keyof typeof mantisTheme.shadows): string => {
  return mantisTheme.shadows[size];
};